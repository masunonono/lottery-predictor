function renderIntervalAnalysis() {
  const el = document.getElementById('interval-analysis');
  if (!el || history.length === 0) return;
  const stats = computeIntervalStats();
  const sorted = Array.from({ length: 43 }, (_, i) => ({ num: i + 1, ...stats[i + 1] }))
    .sort((a, b) => b.duenessScore - a.duenessScore);
  el.innerHTML = sorted.slice(0, 20).map(s => `
    <div class="interval-row">
      <span class="interval-num">${s.num}</span>
      <div class="interval-info">
        <div class="dueness-bar-wrap">
          <div class="dueness-bar" style="width:${s.duenessScore}%"></div>
        </div>
        <span class="interval-meta">スコア${s.duenessScore} / 平均間隔:${s.avgInterval ?? '—'}回 / 最後:${s.lastSeen}回前</span>
      </div>
    </div>
  `).join('');
}

function renderCoOccurrenceAnalysis() {
  const el = document.getElementById('cooccurrence-analysis');
  if (!el || history.length === 0) return;
  const matrix = computeCoOccurrence();
  const pairs = [];
  for (let i = 1; i <= 43; i++) {
    for (let j = i + 1; j <= 43; j++) {
      if (matrix[i][j] > 0) pairs.push({ a: i, b: j, count: matrix[i][j] });
    }
  }
  pairs.sort((a, b) => b.count - a.count);
  el.innerHTML = pairs.slice(0, 15).map(p => `
    <div class="pair-row">
      <span class="pair-nums"><span class="badge">${p.a}</span><span class="pair-plus">＋</span><span class="badge">${p.b}</span></span>
      <span class="pair-count">${p.count}回 同時出現</span>
    </div>
  `).join('');
}

function renderZoneAnalysis() {
  const el = document.getElementById('zone-analysis');
  if (!el || history.length === 0) return;
  const stats = computeZoneStats();
  el.innerHTML = stats.map(z => {
    const pct = Math.round(z.ratio * 100);
    const barW = Math.min(150, pct);
    const color = pct < 90 ? '#74b9ff' : pct > 110 ? '#e17055' : '#00b894';
    const label = pct < 90 ? '少なめ' : pct > 110 ? '多め' : '適正';
    return `
      <div class="zone-card">
        <div class="zone-label">${z.label}</div>
        <div class="zone-bar-wrap">
          <div class="zone-bar" style="width:${barW}%;background:${color}"></div>
        </div>
        <div class="zone-pct" style="color:${color}">${pct}% <span class="zone-tag">${label}</span></div>
        <div class="zone-detail">期待値 ${z.expected.toFixed(2)} / 実績 ${z.actual.toFixed(2)}</div>
      </div>
    `;
  }).join('');
}

