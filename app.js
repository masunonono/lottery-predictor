/** 奇数・偶数の組み合わせ別出現分布 */
function computeOddEvenDist() {
  const dist = {};
  for (let i = 0; i <= config.pickCount; i++) dist[i] = 0;
  history.forEach(r => {
    const odds = r.main.filter(n => n % 2 === 1).length;
    dist[odds]++;
  });
  return dist;
}

/** 合計値のレンジ別分布 */
function computeSumDist() {
  const sums = history.map(r => r.main.reduce((s, n) => s + n, 0));
  const avg = sums.length ? sums.reduce((s, v) => s + v, 0) / sums.length : 0;
  const min = sums.length ? Math.min(...sums) : 0;
  const max = sums.length ? Math.max(...sums) : 0;
  const rangeCounts = (config.sumRanges || []).map(range => ({
    ...range,
    count: sums.filter(s => s >= range.lo && s <= range.hi).length
  }));
  return { avg, min, max, rangeCounts, sums };
}

/** スパン（最大値-最小値）の分布 */
function computeSpanDist() {
  if (history.length === 0) return { avg: 0, dist: [] };
  const spans = history.map(r => Math.max(...r.main) - Math.min(...r.main));
  const avg = spans.reduce((s, v) => s + v, 0) / spans.length;
  const ranges = [
    { label: '〜15',   lo: 0,  hi: 15 },
    { label: '16〜20', lo: 16, hi: 20 },
    { label: '21〜25', lo: 21, hi: 25 },
    { label: '26〜30', lo: 26, hi: 30 },
    { label: '31〜35', lo: 31, hi: 35 },
    { label: '36〜',   lo: 36, hi: 99 },
  ];
  const dist = ranges.map(r => ({ ...r, count: spans.filter(s => s >= r.lo && s <= r.hi).length }));
  return { avg, dist };
}

/** 連続数字（隣り合うペア）の分布 */
function computeConsecDist() {
  const dist = {};
  history.forEach(r => {
    const s = [...r.main].sort((a, b) => a - b);
    let pairs = 0;
    for (let i = 0; i < s.length - 1; i++) if (s[i + 1] - s[i] === 1) pairs++;
    dist[pairs] = (dist[pairs] || 0) + 1;
  });
  return dist;
}

/** 前回との重複数の分布 */
function computeOverlapDist() {
  const dist = {};
  for (let i = 0; i < history.length - 1; i++) {
    const cur  = new Set(history[i].main);
    const prev = new Set(history[i + 1].main);
    let cnt = 0;
    cur.forEach(n => { if (prev.has(n)) cnt++; });
    dist[cnt] = (dist[cnt] || 0) + 1;
  }
  return dist;
}

/** 各数字の繰り越し率（出た次の回にも出る確率） */
function computeCarryoverRates() {
  const appear = new Array(config.maxNum + 1).fill(0);
  const repeat = new Array(config.maxNum + 1).fill(0);
  for (let i = 0; i < history.length - 1; i++) {
    const cur  = history[i].main;
    const next = new Set(history[i + 1].main);
    cur.forEach(n => { appear[n]++; if (next.has(n)) repeat[n]++; });
  }
  return Array.from({ length: config.maxNum }, (_, i) => {
    const n = i + 1;
    return { num: n, rate: appear[n] > 0 ? repeat[n] / appear[n] : 0, appear: appear[n], repeat: repeat[n] };
  }).sort((a, b) => b.rate - a.rate);
}

/** ボーナス番号が翌回の本数字になった確率 */
function computeBonusToMainRates() {
  const appear = new Array(config.maxNum + 1).fill(0);
  const hit    = new Array(config.maxNum + 1).fill(0);
  for (let i = 0; i < history.length - 1; i++) {
    const bonuses = [history[i].bonus];
    if (history[i].bonus2) bonuses.push(history[i].bonus2);
    const nextMain = new Set(history[i + 1].main);
    bonuses.forEach(b => { appear[b]++; if (nextMain.has(b)) hit[b]++; });
  }
  const total = appear.reduce((s, v) => s + v, 0);
  const totalHit = hit.reduce((s, v) => s + v, 0);
  const avg = total > 0 ? totalHit / total : 0;
  return {
    avg,
    items: Array.from({ length: config.maxNum }, (_, i) => {
      const n = i + 1;
      return { num: n, rate: appear[n] > 0 ? hit[n] / appear[n] : 0, appear: appear[n], hit: hit[n] };
    }).sort((a, b) => b.rate - a.rate)
  };
}

/** 月別の合計値平均・出現頻度を集計 */
function computeMonthlyStats() {
  const monthData = {};
  for (let m = 1; m <= 12; m++) monthData[m] = { sums: [], counts: new Array(config.maxNum + 1).fill(0), total: 0 };
  history.forEach(r => {
    if (!r.date) return;
    const m = parseInt(r.date.split('-')[1], 10);
    if (!monthData[m]) return;
    monthData[m].sums.push(r.main.reduce((s, n) => s + n, 0));
    r.main.forEach(n => monthData[m].counts[n]++);
    monthData[m].total++;
  });
  return Object.fromEntries(Object.entries(monthData).map(([m, d]) => [m, {
    avg:    d.sums.length ? d.sums.reduce((s, v) => s + v, 0) / d.sums.length : null,
    min:    d.sums.length ? Math.min(...d.sums) : null,
    max:    d.sums.length ? Math.max(...d.sums) : null,
    counts: d.counts,
    total:  d.total
  }]));
}

// ─── Prediction algorithms ────────────────────────────────────────────────────
function pickRandom6(pool) {
  const arr = [...pool];
  const result = [];
  const n = Math.min(config.pickCount, arr.length);
  while (result.length < n) {
    const idx = Math.floor(Math.random() * arr.length);
    result.push(arr.splice(idx, 1)[0]);
  }
  return result.sort((a, b) => a - b);
}

function pickBonus(main) {
  const bonuses = [];
  while (bonuses.length < config.bonusCount) {
    let b;
    do { b = Math.floor(Math.random() * config.maxNum) + 1; }
    while (main.includes(b) || bonuses.includes(b));
    bonuses.push(b);
  }
  return bonuses; // always array
}

function predictHot(freq) {
  const ranked = getRanked(freq);
  const top = ranked.slice(0, Math.min(15, ranked.length)).map(r => r.num);
  return pickRandom6(top);
}

function predictCold(freq) {
  const ranked = getRanked(freq).reverse();
  const bottom = ranked.slice(0, Math.min(15, ranked.length)).map(r => r.num);
  return pickRandom6(bottom);
}

function predictBalanced(freq) {
  const ranked = getRanked(freq);
  const half = Math.ceil(config.pickCount / 2);
  const hot = ranked.slice(0, 10).map(r => r.num);
  const cold = ranked.slice(-10).map(r => r.num);
  const hotPicks = pickRandom6(hot).slice(0, half);
  const coldPool = cold.filter(n => !hotPicks.includes(n));
  const coldPicks = pickRandom6(coldPool).slice(0, config.pickCount - half);
  return [...hotPicks, ...coldPicks].sort((a, b) => a - b);
}

function predictWeighted(freq) {
  const total = freq.reduce((s, v) => s + v, 0);
  const pick = () => {
    let r = Math.random() * total;
    for (let i = 1; i <= config.maxNum; i++) {
      r -= freq[i];
      if (r <= 0) return i;
    }
    return config.maxNum;
  };
  const result = new Set();
  while (result.size < config.pickCount) result.add(pick());
  return Array.from(result).sort((a, b) => a - b);
}

function predictRandom() {
  const pool = Array.from({ length: config.maxNum }, (_, i) => i + 1);
  return pickRandom6(pool);
}

function predictByInterval(intervalStats) {
  const scored = Array.from({ length: config.maxNum }, (_, i) => ({
    num: i + 1,
    score: intervalStats[i + 1] ? intervalStats[i + 1].duenessScore : 0
  })).sort((a, b) => b.score - a.score);
  const top20 = scored.slice(0, 20);
  const totalScore = top20.reduce((s, v) => s + v.score + 1, 0);
  const pick = () => {
    let r = Math.random() * totalScore;
    for (const item of top20) { r -= (item.score + 1); if (r <= 0) return item.num; }
    return top20[0].num;
  };
  const result = new Set();
  while (result.size < config.pickCount) result.add(pick());
  return Array.from(result).sort((a, b) => a - b);
}

function predictByCoOccurrence(matrix, freq) {
  const ranked = getRanked(freq);
  const top10 = ranked.slice(0, 10).map(r => r.num);
  const seed = top10[Math.floor(Math.random() * top10.length)];
  const result = [seed];
  const all = Array.from({ length: config.maxNum }, (_, i) => i + 1);
  while (result.length < config.pickCount) {
    const candidates = all.filter(n => !result.includes(n));
    candidates.sort((a, b) => {
      const sB = result.reduce((s, r) => s + matrix[r][b], 0);
      const sA = result.reduce((s, r) => s + matrix[r][a], 0);
      return sB - sA;
    });
    result.push(candidates[0]);
  }
  return result.sort((a, b) => a - b);
}

function predictByZoneBalance(zoneStats, freq) {
  const weights = zoneStats.map(z => z.ratio > 0 ? 1 / z.ratio : 2);
  const totalW = weights.reduce((s, v) => s + v, 0);
  const rawAlloc = weights.map(w => (w / totalW) * config.pickCount);
  const alloc = rawAlloc.map(v => Math.floor(v));
  const remainder = config.pickCount - alloc.reduce((s, v) => s + v, 0);
  rawAlloc.map((v, i) => ({ i, frac: v - Math.floor(v) }))
    .sort((a, b) => b.frac - a.frac)
    .slice(0, remainder)
    .forEach(f => alloc[f.i]++);
  const result = [];
  const used = new Set();
  config.zones.forEach((zone, zi) => {
    const pool = [];
    for (let n = zone.min; n <= zone.max; n++) pool.push({ num: n, count: freq[n] });
    pool.sort((a, b) => b.count - a.count);
    const available = pool.map(z => z.num).filter(n => !used.has(n));
    const picks = Math.min(alloc[zi], available.length);
    for (let k = 0; k < picks; k++) {
      const candidates = available.filter(n => !used.has(n)).slice(0, Math.max(5, picks + 2));
      const totalF = candidates.reduce((s, n) => s + freq[n] + 1, 0);
      let r = Math.random() * totalF;
      let chosen = candidates[0];
      for (const n of candidates) { r -= freq[n] + 1; if (r <= 0) { chosen = n; break; } }
      result.push(chosen);
      used.add(chosen);
    }
  });
  if (result.length < config.pickCount) {
    Array.from({ length: config.maxNum }, (_, i) => i + 1)
      .filter(n => !used.has(n))
      .sort((a, b) => freq[b] - freq[a])
      .slice(0, config.pickCount - result.length)
      .forEach(n => result.push(n));
  }
  return result.sort((a, b) => a - b);
}

function predictComposite(freq, intervalStats, matrix) {
  const freqVals = Array.from({ length: config.maxNum }, (_, i) => freq[i + 1]);
  const freqMax = Math.max(...freqVals);
  const freqMin = Math.min(...freqVals);
  const coSums = Array.from({ length: config.maxNum + 1 }, (_, n) => n === 0 ? 0 : matrix[n].reduce((s, v) => s + v, 0));
  const coMax = Math.max(...coSums.slice(1));
  const coMin = Math.min(...coSums.slice(1));
  const scored = Array.from({ length: config.maxNum }, (_, i) => {
    const n = i + 1;
    const freqScore = freqMax > freqMin ? (freq[n] - freqMin) / (freqMax - freqMin) * 100 : 50;
    const dueness = intervalStats[n] ? intervalStats[n].duenessScore : 50;
    const coScore = coMax > coMin ? (coSums[n] - coMin) / (coMax - coMin) * 100 : 50;
    return { num: n, score: 0.3 * freqScore + 0.4 * dueness + 0.3 * coScore };
  }).sort((a, b) => b.score - a.score);
  const top20 = scored.slice(0, 20);
  const totalScore = top20.reduce((s, v) => s + v.score + 1, 0);
  const pick = () => {
    let r = Math.random() * totalScore;
    for (const item of top20) { r -= (item.score + 1); if (r <= 0) return item.num; }
    return top20[0].num;
  };
  const result = new Set();
  while (result.size < config.pickCount) result.add(pick());
  return Array.from(result).sort((a, b) => a - b);
}

/** 季節（現在月）の傾向に合わせた合計値帯で予測 */
function predictBySeason(freq) {
  const currentMonth = new Date().getMonth() + 1;
  const monthly = computeMonthlyStats();
  const mData = monthly[currentMonth];
  // 当月データがなければ通常の合計値絞り込みにフォールバック
  if (!mData || mData.total < 3) return predictBySumRange(freq);
  // 当月の平均合計値 ±15 を目標帯に設定
  const margin = 15;
  const lo = Math.round(mData.avg - margin);
  const hi = Math.round(mData.avg + margin);
  // 当月の出現頻度を加味した重みづけ頻度も使用
  const blendedFreq = freq.map((v, i) => {
    if (i === 0) return 0;
    const mCount = mData.counts[i] || 0;
    const mExpected = mData.total * config.pickCount / config.maxNum;
    const mBoost = mExpected > 0 ? mCount / mExpected : 1;
    return v * (0.7 + 0.3 * mBoost);
  });
  for (let attempt = 0; attempt < 200; attempt++) {
    const nums = predictWeighted(blendedFreq);
    const total = nums.reduce((s, n) => s + n, 0);
    if (total >= lo && total <= hi) return nums;
  }
  return predictWeighted(blendedFreq);
}

/** ヘルパー: 番号セットが統計条件を満たすか */
function checkStatConditions(nums) {
  const sorted = [...nums].sort((a, b) => a - b);
  const odds  = nums.filter(n => n % 2 === 1).length;
  const total = nums.reduce((s, n) => s + n, 0);
  const span  = sorted[sorted.length - 1] - sorted[0];
  let consecPairs = 0;
  for (let i = 0; i < sorted.length - 1; i++) if (sorted[i + 1] - sorted[i] === 1) consecPairs++;
  return {
    oddOk:    odds === config.bestOddCount,
    sumOk:    total >= config.sumOptimal[0] && total <= config.sumOptimal[1],
    spanOk:   span  >= config.spanOptimal[0] && span  <= config.spanOptimal[1],
    consecOk: consecPairs >= 1,
    odds, total, span, consecPairs
  };
}

/** 奇偶バランス予測: ゲームの最頻奇偶パターンに合わせて選択 */
function predictByOddEven(freq) {
  const target = config.bestOddCount;
  const oddPool  = Array.from({ length: config.maxNum }, (_, i) => i + 1).filter(n => n % 2 === 1);
  const evenPool = Array.from({ length: config.maxNum }, (_, i) => i + 1).filter(n => n % 2 === 0);
  const pickWeighted = (pool) => {
    const total = pool.reduce((s, n) => s + freq[n] + 1, 0);
    let r = Math.random() * total;
    for (const n of pool) { r -= freq[n] + 1; if (r <= 0) return n; }
    return pool[pool.length - 1];
  };
  const result = new Set();
  const oddArr  = [...oddPool].sort(() => Math.random() - 0.5);
  const evenArr = [...evenPool].sort(() => Math.random() - 0.5);
  // 頻度加重でtarget個の奇数を選ぶ
  for (let i = 0; i < 30 && result.size < target; i++) {
    const candidates = oddArr.filter(n => !result.has(n));
    if (candidates.length === 0) break;
    result.add(pickWeighted(candidates));
  }
  for (let i = 0; i < 30 && result.size < config.pickCount; i++) {
    const candidates = evenArr.filter(n => !result.has(n));
    if (candidates.length === 0) break;
    result.add(pickWeighted(candidates));
  }
  while (result.size < config.pickCount) {
    const n = Math.floor(Math.random() * config.maxNum) + 1;
    result.add(n);
  }
  return Array.from(result).sort((a, b) => a - b);
}

/** 合計値絞り込み予測: 統計的中央帯の合計値に収まる組み合わせを生成 */
function predictBySumRange(freq) {
  const [lo, hi] = config.sumOptimal;
  for (let attempt = 0; attempt < 200; attempt++) {
    const nums = predictWeighted(freq);
    const total = nums.reduce((s, n) => s + n, 0);
    if (total >= lo && total <= hi) return nums;
  }
  return predictWeighted(freq); // fallback
}

/** 繰り越し重視予測: 繰り越し率の高い番号を1〜2個含める */
function predictByCarryover(freq) {
  if (history.length < 2) return predictHot(freq);
  const carryRates = computeCarryoverRates();
  const lastMain   = new Set(history[0].main);
  // 前回出た番号のうち繰り越し率が高いもの
  const carryover = carryRates
    .filter(r => lastMain.has(r.num) && r.rate > 0)
    .sort((a, b) => b.rate - a.rate);
  const result = new Set();
  // 繰り越し率上位から1〜2個採用
  const carryCount = Math.min(2, carryover.length);
  for (let i = 0; i < carryCount; i++) result.add(carryover[i].num);
  // 残りはホット数字から補充
  const ranked = getRanked(freq);
  for (const r of ranked) {
    if (result.size >= config.pickCount) break;
    if (!result.has(r.num)) result.add(r.num);
  }
  return Array.from(result).sort((a, b) => a - b);
}

/** ボーナス翌回予測: 直近ボーナス番号の転換率を活用 */
function predictByBonusFollowup(freq) {
  if (history.length < 2) return predictHot(freq);
  const b2mRates = computeBonusToMainRates();
  const rateMap  = Object.fromEntries(b2mRates.items.map(r => [r.num, r.rate]));
  const lastBonuses = [history[0].bonus];
  if (history[0].bonus2) lastBonuses.push(history[0].bonus2);
  // ボーナス番号のうち転換率の高いものを優先候補に
  const bonusCandidates = lastBonuses
    .sort((a, b) => (rateMap[b] || 0) - (rateMap[a] || 0));
  const result = new Set();
  // 転換率が平均より高いボーナス番号を採用
  const avgRate = b2mRates.avg;
  bonusCandidates.forEach(b => {
    if ((rateMap[b] || 0) > avgRate && result.size < Math.ceil(config.pickCount / 3)) {
      result.add(b);
    }
  });
  // 残りをホット数字で補充
  const ranked = getRanked(freq);
  for (const r of ranked) {
    if (result.size >= config.pickCount) break;
    if (!result.has(r.num)) result.add(r.num);
  }
  return Array.from(result).sort((a, b) => a - b);
}

/** 統計フィルター総合予測: 奇偶・合計・連続・スパンの統計条件を全て満たす組み合わせ */
function predictByStatFilter(freq, intervalStats, matrix) {
  // ベース生成関数（複合スコア）
  const base = () => predictComposite(freq, intervalStats, matrix);
  let best = null;
  let bestScore = -1;
  for (let attempt = 0; attempt < 300; attempt++) {
    const nums = base();
    const cond = checkStatConditions(nums);
    // 条件スコア: 奇偶1点、合計1点、スパン1点、連続1点
    const score = (cond.oddOk ? 1 : 0) + (cond.sumOk ? 1 : 0)
                + (cond.spanOk ? 1 : 0) + (cond.consecOk ? 1 : 0);
    if (score > bestScore) { best = nums; bestScore = score; }
    if (bestScore === 4) break; // 全条件クリア
  }
  return best || predictComposite(freq, intervalStats, matrix);
}

function makePredictionResult(main, note) {
  const bonuses = pickBonus(main);
  return { main, bonus: bonuses[0], bonus2: bonuses[1] || null, note };
}

function generatePrediction(method) {
  if (history.length === 0) {
    const main = predictRandom();
    return makePredictionResult(main, 'データがないためランダム生成しました。');
  }
  const freq = computeFrequency();
  let main;
  let note;
  switch (method) {
    case 'hot':
      main = predictHot(freq);
      note = '出現頻度の高い数字を中心に選択しました。';
      break;
    case 'cold':
      main = predictCold(freq);
      note = '出現頻度の低い数字（そろそろ出そうな数字）を中心に選択しました。';
      break;
    case 'balanced':
      main = predictBalanced(freq);
      note = 'ホット数字とコールド数字をバランスよく組み合わせました。';
      break;
    case 'frequency-weighted':
      main = predictWeighted(freq);
      note = '各数字の出現頻度を重みとした確率的選択を行いました。';
      break;
    case 'interval': {
      const iStats = computeIntervalStats();
      main = predictByInterval(iStats);
      note = '出現間隔が平均を超えている「そろそろ出そうな」数字を優先して選択しました。';
      break;
    }
    case 'cooccurrence': {
      const matrix = computeCoOccurrence();
      main = predictByCoOccurrence(matrix, freq);
      note = '過去に同時出現しやすかった数字の組み合わせを活用して選択しました。';
      break;
    }
    case 'zone-balance': {
      const zStats = computeZoneStats();
      main = predictByZoneBalance(zStats, freq);
      note = '数字帯（ゾーン）の出現バランスを考慮して選択しました。';
      break;
    }
    case 'composite': {
      const iStats2 = computeIntervalStats();
      const matrix2 = computeCoOccurrence();
      main = predictComposite(freq, iStats2, matrix2);
      note = '出現頻度・出現間隔・共起傾向を総合スコアで評価して選択しました。';
      break;
    }
    case 'season': {
      const currentMonth = new Date().getMonth() + 1;
      const monthly = computeMonthlyStats();
      const mData = monthly[currentMonth];
      main = predictBySeason(freq);
      const avgStr = mData && mData.avg ? mData.avg.toFixed(1) : '—';
      note = `${currentMonth}月の過去平均合計値（${avgStr}）付近を目標に、当月の出現傾向を加味して選択しました。`;
      break;
    }
    case 'odd-even':
      main = predictByOddEven(freq);
      note = `${config.name}の最頻出パターン（奇数${config.bestOddCount}個＋偶数${config.pickCount - config.bestOddCount}個）に合わせて選択しました。`;
      break;
    case 'sum-range':
      main = predictBySumRange(freq);
      note = `合計値が統計的中央帯（${config.sumOptimal[0]}〜${config.sumOptimal[1]}）に収まる組み合わせを選択しました。`;
      break;
    case 'carryover':
      main = predictByCarryover(freq);
      note = '繰り越し率の高い前回番号を引き継ぎつつ、ホット数字で補完しました。';
      break;
    case 'bonus-followup':
      main = predictByBonusFollowup(freq);
      note = '直近のボーナス番号のうち翌回本数字になりやすいものを優先採用しました。';
      break;
    case 'stat-filter': {
      const iStats3 = computeIntervalStats();
      const matrix3 = computeCoOccurrence();
      main = predictByStatFilter(freq, iStats3, matrix3);
      const cond = checkStatConditions(main);
      note = `統計条件チェック — 奇偶:${cond.oddOk?'✓':'△'} 合計:${cond.sumOk?'✓':'△'}(${cond.total}) スパン:${cond.spanOk?'✓':'△'}(${cond.span}) 連続:${cond.consecOk?'✓':'△'}(${cond.consecPairs}ペア)`;
      break;
    }
    default:
      main = predictRandom();
      note = '完全ランダムで生成しました。';
  }
  return makePredictionResult(main, note);
}

